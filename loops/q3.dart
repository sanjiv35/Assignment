//Can you give an example of a problem that can be solved using both a for loop and a while loop?
void main(List<String> args) {
  //for loop for multiplication table
  for (int i = 1; i <= 10; i++) {
    print(5 * i);
  }

  //while loop for multiplication
  int j = 1;
  while (j <= 10) {
    print(6 * j);
    j++;
  }
}
